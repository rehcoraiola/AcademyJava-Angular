package com.exemplo.demo.models;

public class Categoria {
    private String nome;
    private String desc;

    public void setDesc(String desc) {
        this.desc = desc;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public String getDesc() {
        return desc;
    }
    public String getNome() {
        return nome;
    }
}
